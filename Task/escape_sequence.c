//Write a program to use escape sequence
#include<stdio.h>

int main()
{
    // use of \n
    printf("Address\nVillage: Dholidhar \n");
    
    // use of \t
    printf("\tTaluka: Jam Kandorana \n");
    
    // use of \r
    printf("District:\rRajkot \n");

    // use of \b
    printf("Gujrat\b-360405 \n");

    // use of \f
    printf("what is programme\f-it is a set of instruction \n");

    // use of \v
    printf("what is programming\v-to creat a program \n");

    return 0;
}